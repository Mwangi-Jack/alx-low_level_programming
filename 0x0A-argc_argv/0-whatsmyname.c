#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: program to print its name
 *
 * @argv: an array of arguments
 *
 * @argc: the length of the array
 *
 * Return: Always Success (0)
*/

int main(int __attribute__ ((unused)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}

